package pratamawijaya.moviesinfokotlin.data.model.mapper;

import java.util.ArrayList;
import java.util.List;
import javax.inject.Inject;
import pratamawijaya.moviesinfokotlin.data.model.MovieModel;
import pratamawijaya.moviesinfokotlin.domain.entity.Movie;

/**
 * Created by Pratama Nur Wijaya
 * Date : Oct - 10/15/16
 * Project Name : MoviesInfoKotlin
 */

public class MovieModelMapper {

  @Inject public MovieModelMapper() {
  }

  public Movie transform(MovieModel movieModel) {
    Movie movie = null;
    if (movieModel != null) {
      movie = new Movie(movieModel.getId());
      movie.setPosterPath(movieModel.getPosterPath());
      movie.setAdult(movieModel.getAdult());
      movie.setOverview(movieModel.getOverview());
      movie.setReleaseDate(movieModel.getReleaseDate());
      movie.setGenreIds(movieModel.getGenreIds());
      movie.setOriginalTitle(movieModel.getOriginalTitle());
      movie.setOriginalLanguage(movieModel.getOriginalLanguage());
      movie.setTitle(movieModel.getTitle());
      movie.setBackdropPath(movieModel.getBackdropPath());
      movie.setPopularity(movieModel.getPopularity());
      movie.setVoteCount(movieModel.getVoteCount());
      movie.setVideo(movieModel.getVideo());
      movie.setVoteAverage(movieModel.getVoteAverage());
    }
    return movie;
  }

  public List<Movie> transform(List<MovieModel> movies) {
    List<Movie> movieList = new ArrayList<>();
    Movie movie;
    for (MovieModel data : movies) {
      movie = transform(data);
      if (movie != null) {
        movieList.add(movie);
      }
    }
    return movieList;
  }
}
